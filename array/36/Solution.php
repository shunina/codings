<?php

Class Solution {

    /**
 *      * @param String[][] $board
 *           * @return Boolean
 *                */
    function isValidSudoku($board) {
      $length = count($board);
      $column = [];
      for ($i=0; $i < $length; $i++){
        $row = [];
        for ($j=0; $j < $length; $j++){
          if($i%3===2 && $j%3===2){
            $box=[];
            $l = 0;
            for ($k=0; $k<3; $k++){
              for ($l=0; $l<3; $l++){
                if($board[$i-$k][$j-$l] === '.'){
                  continue;
                }

                if(isset($box[$board[$i-$k][$j-$l]])){
                  return false;
                }
                $box[$board[$i-$k][$j-$l]] = true;
              }
            }
          }
          
          if($board[$i][$j] === '.'){
            continue;
          }
          if(isset($row[$board[$i][$j]]) || isset($column[$j][$board[$i][$j]]) ){
            return false;
          }
          $row[$board[$i][$j]] = true;
          $column[$j][$board[$i][$j]] = true;
        }
      }
      return true;
    }
}

